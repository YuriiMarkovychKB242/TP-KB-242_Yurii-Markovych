text = "  hello python world!  "

print(f"Оригінал: '{text}'")
print(f"strip(): '{text.strip()}'")        # Видаляє пробіли по краях
print(f"capitalize(): '{text.strip().capitalize()}'") # Перша літера велика
print(f"title(): '{text.strip().title()}'")           # Кожне слово з великої
print(f"upper(): '{text.upper()}'")                   # Всі великі
print(f"lower(): '{text.lower()}'")                   # Всі маленькі